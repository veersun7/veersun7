public class MathOperations {
    public static void main(String[] args) {
        int add = 20;
        add += 7;
        System.out.println(add + "- Addition Total");
        int subtract = 35;
        subtract -= 9;
        System.out.println(subtract + "- Subtraction Total");
        int multiply = 150;
        multiply *= 4;
        System.out.println(multiply + "- Multiply Total");
        int division = 136;
        division /= 2;
        System.out.println(division + "- Division Total");
    }
}
