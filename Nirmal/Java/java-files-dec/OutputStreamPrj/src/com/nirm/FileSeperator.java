package com.nirm;

import java.io.File;

public class FileSeperator {

	public static void main(String[] args) {
		System.out.println("File Seperator: " +File.separator);
		System.out.println("File Seperator character: " +File.separatorChar);
		
		System.out.println("File Path Seperator: " +File.pathSeparator);
		System.out.println("File Path Seperator character: " +File.pathSeparatorChar);
	}

}