package P2;
 
public class message {

	public void display(String m1) {
		System.out.println("Enter one message");
		System.out.println(m1);
	}
	
	public void display(String m1, String m2) {
		System.out.println("Enter two message");
		System.out.println(m1);
		System.out.println(m2);
	
	}
	
	public void display(String m1, String m2, String m3) {
		System.out.println("Enter three message");
		System.out.println(m1);
		System.out.println(m2);
		System.out.println(m3);
	}
}
